package spire.core.arangodb.example.bean;

/**
 *
 * @author ranjeet
 */
public class ResponseBean {

    private String m;
    private String s;
    private String r;
    private double w;

    public ResponseBean() {
    }

    public String getM() {
        return m;
    }

    public void setM(String m) {
        this.m = m;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public String getR() {
        return r;
    }

    public void setR(String r) {
        this.r = r;
    }

    public double getW() {
        return w;
    }

    public void setW(double w) {
        this.w = w;
    }

}
